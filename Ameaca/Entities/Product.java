package Ameaca.Entities;

public class Product {

    private String name, version;
    private int id;

    public String getName() {
        return name;
    }

    public String getVersion() {
        return version;
    }

    public int getID() {
        return id;
    }

    public void setName(String value) {
        name = value;
    }

    public void setVersion(String value) {
        version = value;
    }
}
